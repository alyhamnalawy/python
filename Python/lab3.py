# ask the user for two numbers
num1 = int(input("Enter the first number: "))
num2 = int(input("Enter the second number: "))

# math operations
sum=num1+num2
difference=num1-num2
product=num1*num2
divide=num1/num2

# logical operators
or_op=num1|num2
and_op=num1&num2
xor_op=num1^num2

# print the results
print("The sum of the two numbers is: ", sum)
print("The difference between the two numbers is: ", difference)
print("The product of the two numbers is: ", product)
print("The divide of the two numbers is: ", divide)
print("The or operation is: ",or_op)
print("The and operation is: ",and_op)
print("The xor operation is: ",xor_op)